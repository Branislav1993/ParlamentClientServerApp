    private void fillSessionInfo() {
        int index = tableSessions.getSelectedRow();
        SessionsTableModel model = (SessionsTableModel) tableSessions.getModel();
        PlenarySession ps = model.getSessionByIndex(index);
        if (ps == null) {
            return;
        }
        textAreaAgenda.setText(ps.getAgenda());
        TextAreaTranscript.setText(ps.getTranscriptText());
    }

    public void fillSessionTable() {
        try {
            SessionsTableModel model = new SessionsTableModel(Controller.getInstance().getSessions());
            tableSessions.setModel(model);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Couldn't get sessions from server!", "Error!", JOptionPane.ERROR_MESSAGE);
        }
    }

