    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        String searchCriteria = txtSearchSessions.getText().trim();
        try {
            List<PlenarySession> sessions = Controller.getInstance().searchSessions(searchCriteria);
            tableSessions.setModel(new SessionsTableModel(sessions));
            if (sessions.size() > 0) {
                JOptionPane.showMessageDialog(this, "System found matching results!", "Success!", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "There are no matching results!", "Error!", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Coldn't get sessions from server!", "Error!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSearchActionPerformed

    private void btnDeleteSessionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteSessionActionPerformed
        int index = tableSessions.getSelectedRow();
        tableSessions.getSelectionModel().clearSelection();
        if (index == -1) {
            JOptionPane.showMessageDialog(this, "Select session to delete!", "Error!", JOptionPane.ERROR_MESSAGE);
        } else {
            SessionsTableModel model = (SessionsTableModel) tableSessions.getModel();
            PlenarySession ps = model.getSessionByIndex(index);
            try {
                Controller.getInstance().deleteSession(ps);
                JOptionPane.showMessageDialog(this, "Session successfully deleted!", "Success!", JOptionPane.INFORMATION_MESSAGE);
                model.deleteSessionByIndex(index);
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Session was not deleted!", "Error!", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnDeleteSessionActionPerformed

    private void btnAddSessionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddSessionActionPerformed
        if (addSessionForm == null || !addSessionForm.isDisplayable()) {
            addSessionForm = new AddSessionForm(this);
            addSessionForm.setVisible(true);
            addSessionForm.setLocationRelativeTo(this);
        } else {
            addSessionForm.toFront();
            addSessionForm.repaint();
        }
    }//GEN-LAST:event_btnAddSessionActionPerformed

    private void btnUpdateSessionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateSessionActionPerformed
         int index = tableSessions.getSelectedRow();
        if (index == -1) {
            JOptionPane.showMessageDialog(this, "Select session to update!", "Error!", JOptionPane.ERROR_MESSAGE);
        } else {
            SessionsTableModel model = (SessionsTableModel) tableSessions.getModel();
            PlenarySession ps = model.getSessionByIndex(index);
            if (addSessionForm == null || !addSessionForm.isDisplayable()) {
                addSessionForm = new AddSessionForm(this, ps);
                addSessionForm.setVisible(true);
                addSessionForm.setLocationRelativeTo(this);
            } else {
                addSessionForm.toFront();
                addSessionForm.repaint();
            }
        }
    }//GEN-LAST:event_btnUpdateSessionActionPerformed

